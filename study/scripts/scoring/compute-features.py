#!/usr/bin/env python3
"""Compute text-statistical features on every PR spec.

No LLM calls — pure text analysis. One pass, comprehensive feature set.
Outputs data/pr-features.csv with one row per PR.

Features are organized into categories:
1. AUTHOR CLASSIFICATION — is the author a bot/AI agent?
2. AI SELF-TAGS — does the text declare itself AI-generated?
3. HUMANNESS SIGNALS — typos, casual language, questions, uncertainty
4. ORGANIZATIONAL CONTEXT — references only a human could make
5. REASONING QUALITY — negation depth, causal chains, tradeoffs
6. TEMPLATE/SLOP — performative structure without substance
7. TEXT STATISTICS — burstiness, vocabulary diversity, word length

Usage:
    python3 compute-features.py
"""

