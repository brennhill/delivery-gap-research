def _compute_perplexity_batch(features: list[dict], prs_by_key: dict) -> None:
    """Compute GPT-2 perplexity for each PR body. Modifies features in place."""
    try:
        import torch
        from transformers import GPT2LMHeadModel, GPT2Tokenizer
        import math
    except ImportError:
        print("  Skipping perplexity (install: pip install transformers torch)")
        return

    print("  Loading GPT-2 for perplexity...", flush=True)
    tokenizer = GPT2Tokenizer.from_pretrained("gpt2")
    model = GPT2LMHeadModel.from_pretrained("gpt2")
    model.eval()

    scored = 0
    for i, feat in enumerate(features):
        key = (feat['repo'], feat['pr_number'])
        pr = prs_by_key.get(key)
        if not pr:
            continue
        body = (pr.get('body') or '')[:2000]
        if len(body) < 50:
            continue

        tokens = tokenizer.encode(body, return_tensors="pt", truncation=True, max_length=512)
        if tokens.shape[1] < 2:
            continue

        try:
            with torch.no_grad():
                outputs = model(tokens, labels=tokens)
                loss_val = outputs.loss.item()
                if math.isfinite(loss_val) and loss_val < 700:
                    feat['perplexity'] = round(math.exp(loss_val), 1)
                else:
                    feat['perplexity'] = 0  # overflow guard
        except Exception:
            continue  # skip this PR, don't abort the batch

        scored += 1
        if scored % 500 == 0:
            print(f"    [{scored} PRs scored for perplexity]", flush=True)

    print(f"  Perplexity computed for {scored} PRs", flush=True)


