# ═══════════════════════════════════════════════════════════════════════
# 2. AI SELF-TAGS (ground truth)
# ═══════════════════════════════════════════════════════════════════════

AI_TAG_RE = re.compile(
    r'(?:Generated with|Claude Code|written (?:with|by) (?:claude|codex|copilot|cursor|devin)|'
    r'🤖|CURSOR_AGENT|coderabbit\.ai|auto-generated comment|'
    r'Generated by|AI-generated|Authored by .+(?:AI|agent|bot))',
    re.IGNORECASE,
)

# Structured AI Disclosure section (used by some repos as a PR template field)
# Matches both "AI Disclosure" and "AI Usage" section headers
AI_DISCLOSURE_RE = re.compile(
    r'(?:##\s*)?(?:AI Disclosure|AI Usage)[:\s#]*\n+(.*?)(?:\n\n|\n##|\Z)',
    re.IGNORECASE | re.DOTALL,
)
# Patterns that indicate "no AI used" in a disclosure section
AI_DISCLOSURE_NEGATIVE_RE = re.compile(
    r'(?:no ai|not used|none|n/a|no,\s|nope|no usage)',
    re.IGNORECASE,
)
# Patterns that indicate "AI was used" in a disclosure section
AI_DISCLOSURE_POSITIVE_RE = re.compile(
    r'(?:ai (?:was )?used|ai assist|cursor|copilot|claude|chatgpt|gpt-[45]|gpt[45]|'
    r'openai|gemini|ai tool|ai.generated|used ai|with ai|ai help|'
    r'implemented using|authored by devin|created by devin|'
    r'antigravity|codex|openclaw|opencode)',
    re.IGNORECASE,
)


def _check_ai_disclosure(body: str) -> bool | None:
    """Parse structured AI Disclosure sections in PR body.

    Returns True if disclosure says AI was used, False if it says no AI,
    None if no disclosure section found or content is ambiguous/template.
    """
    m = AI_DISCLOSURE_RE.search(body)
    if not m:
        return None
    val = m.group(1).strip()
    if not val or val.startswith('<!--'):
        return None  # Template placeholder, not filled in
    if AI_DISCLOSURE_NEGATIVE_RE.search(val):
        return False
    if AI_DISCLOSURE_POSITIVE_RE.search(val):
        return True
    return None  # Ambiguous

