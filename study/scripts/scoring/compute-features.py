# 3. HUMANNESS SIGNALS
# ═══════════════════════════════════════════════════════════════════════

# Typos — robots don't misspell (5x signal)
TYPO_RE = re.compile(
    r'\b(?:teh|taht|waht|hte|becuase|becasue|beacuse|wiht|adn|recieve|occured|'
    r'occurence|seperator|seperate|definately|accomodate|thier|accidently|'
    r'unfortunatly|unfortuantely|wierd|doesnt|dont|cant|wont|isnt|hasnt|'
    r'havent|shouldnt|wouldnt|didnt|ive |im |itll|thats|whats|heres|theres|'
    r'wanna|gonna|gotta|kinda|sorta)\b',
    re.IGNORECASE,
)

# Casual/informal — robots don't use slang (2x signal)
CASUAL_RE = re.compile(
    r'\b(?:hmm|hm|ugh|oops|whoops|argh|yep|nope|yup|nah|btw|fwiw|imo|imho|'
    r'afaik|afaict|tbh|lol|lgtm|wip|nit:|nit |FYI|TIL |IIRC|OTOH|FWIW)\b',
    re.IGNORECASE,
)

# Questions — strongest human signal (16x). AI almost never admits uncertainty.
QUESTION_RE = re.compile(
    r'(?:should we|open question|do we need|not sure (?:if|whether)|TBD|'
    r'to be determined|thoughts\?|feedback\?|what do you think|'
    r'wondering if|is this the right|does this make sense|any concerns|'
    r'am I missing|could this cause|would it be better)',
    re.IGNORECASE,
)

# First person EXPERIENCE narration — "I ran into", "I noticed"
# (Note: AI mimics this at higher rates! Must be combined with other signals)
FIRST_PERSON_EXP_RE = re.compile(
    r'\b(?:I ran into|I found that|I noticed|I tried|I checked|I was|'
    r'I had to|I got|I debugged|I looked into|I realized|I.m not sure|'
    r'I understand|I suspect|I think we|I.ve been)\b',
    re.IGNORECASE,
)

# First person ACTION — "I updated", "I implemented" (AI pattern)
FIRST_PERSON_ACT_RE = re.compile(
    r'\b(?:I (?:updated|implemented|added|removed|changed|fixed|refactored|'
    r'created|modified|replaced|introduced|migrated|converted|ensured))\b',
    re.IGNORECASE,
)

# @mentions of people (not bots, npm scoped packages, or doc-tag keywords)
AT_MENTION_RE = re.compile(r'@[a-zA-Z][a-zA-Z0-9_-]{1,38}')
# Matches followed by '/' are npm scoped packages, not people
AT_SCOPED_PKG_RE = re.compile(r'@[a-zA-Z][a-zA-Z0-9_-]{1,38}/')
BOT_MENTION_FILTER = re.compile(
    r'bot|copilot|coderabbit|renovate|dependabot|biomejs|cloudflare|types',
    re.IGNORECASE,
)
# JSDoc/JavaDoc keywords that look like @mentions but aren't people
DOC_TAG_KEYWORDS = {
    '@param', '@returns', '@return', '@override', '@deprecated',
    '@throws', '@type', '@typedef', '@property', '@interface',
}

